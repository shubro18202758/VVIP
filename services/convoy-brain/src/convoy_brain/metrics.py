"""Prometheus metrics instrumentation for convoy-brain.

Exports metrics for:
- LLM token generation speed and inference latency
- MCP tool execution latency and error rates
- API endpoint request duration and status codes
- Orchestrator reasoning loop detection
"""

from __future__ import annotations

import time
from contextlib import asynccontextmanager
from typing import AsyncIterator

from prometheus_client import (
    Counter,
    Gauge,
    Histogram,
    Info,
    generate_latest,
    CONTENT_TYPE_LATEST,
)

# ─── LLM / Ollama Metrics ────────────────────────────────────────

LLM_TOKENS_GENERATED = Counter(
    "vvip_llm_tokens_generated_total",
    "Total tokens generated by Qwen LLM",
    ["model"],
)

LLM_INFERENCE_DURATION = Histogram(
    "vvip_llm_inference_duration_seconds",
    "LLM inference latency in seconds",
    ["model"],
    buckets=[0.5, 1, 2, 5, 10, 15, 20, 30, 45, 60, 90, 120],
)

OLLAMA_HEALTH = Gauge(
    "vvip_ollama_health_up",
    "Whether Ollama API is reachable (1=up, 0=down)",
)

LLM_TOKENS_PER_SEC = Gauge(
    "vvip_llm_tokens_per_second",
    "Current LLM token generation rate",
    ["model"],
)

# ─── MCP Tool Metrics ────────────────────────────────────────────

MCP_TOOL_CALLS = Counter(
    "vvip_mcp_tool_calls_total",
    "Total MCP tool invocations",
    ["tool_name"],
)

MCP_TOOL_DURATION = Histogram(
    "vvip_mcp_tool_duration_seconds",
    "MCP tool execution latency",
    ["tool_name"],
    buckets=[0.01, 0.05, 0.1, 0.25, 0.5, 1, 2, 5, 10, 30],
)

MCP_TOOL_ERRORS = Counter(
    "vvip_mcp_tool_errors_total",
    "MCP tool execution errors",
    ["tool_name", "error"],
)

# ─── API Metrics ──────────────────────────────────────────────────

API_REQUEST_DURATION = Histogram(
    "vvip_api_request_duration_seconds",
    "API request duration",
    ["service", "method", "endpoint", "status"],
    buckets=[0.01, 0.05, 0.1, 0.25, 0.5, 1, 2, 5, 10, 30],
)

API_REQUESTS_TOTAL = Counter(
    "vvip_api_requests_total",
    "Total API requests",
    ["service", "method", "endpoint", "status"],
)

# ─── Orchestrator Metrics ─────────────────────────────────────────

ORCHESTRATOR_TOOL_ROUNDS = Gauge(
    "vvip_orchestrator_tool_rounds_total",
    "Number of tool rounds in current/last orchestrator turn",
    ["movement_id"],
)

ORCHESTRATOR_NONDETERMINISTIC_ERRORS = Counter(
    "vvip_orchestrator_nondeterministic_errors_total",
    "Non-deterministic failures (different output for same input)",
)

ORCHESTRATOR_TURNS = Counter(
    "vvip_orchestrator_turns_total",
    "Total orchestrator turns",
    ["workflow"],
)

