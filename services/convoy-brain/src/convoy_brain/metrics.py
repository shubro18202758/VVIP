# ─── Helper Decorators ───────────────────────────────────────────

@asynccontextmanager
async def track_llm_call(model: str = "qwen3.5:9b-q4_K_M") -> AsyncIterator[dict]:
    """Context manager that tracks LLM call duration and token count.

    Usage:
        async with track_llm_call() as tracker:
            response = await ollama.generate(prompt)
            tracker["tokens"] = response.eval_count
    """
    tracker: dict = {"tokens": 0}
    start = time.monotonic()
    try:
        yield tracker
    finally:
        duration = time.monotonic() - start
        LLM_INFERENCE_DURATION.labels(model=model).observe(duration)
        tokens = tracker.get("tokens", 0)
        if tokens > 0:
            LLM_TOKENS_GENERATED.labels(model=model).inc(tokens)
            if duration > 0:
                LLM_TOKENS_PER_SEC.labels(model=model).set(tokens / duration)


@asynccontextmanager
async def track_mcp_tool(tool_name: str) -> AsyncIterator[None]:
    """Context manager that tracks MCP tool call duration and errors."""
    MCP_TOOL_CALLS.labels(tool_name=tool_name).inc()
    start = time.monotonic()
    try:
        yield
    except TimeoutError:
        MCP_TOOL_ERRORS.labels(tool_name=tool_name, error="timeout").inc()
        raise
    except Exception as e:
        MCP_TOOL_ERRORS.labels(tool_name=tool_name, error=type(e).__name__).inc()
        raise
    finally:
        duration = time.monotonic() - start
        MCP_TOOL_DURATION.labels(tool_name=tool_name).observe(duration)


def record_api_request(
    method: str, endpoint: str, status: int, duration: float
) -> None:
    """Record API request metrics."""
    labels = {
        "service": "convoy-brain",
        "method": method,
        "endpoint": endpoint,
        "status": str(status),
    }
    API_REQUEST_DURATION.labels(**labels).observe(duration)
    API_REQUESTS_TOTAL.labels(**labels).inc()


def get_metrics_response() -> tuple[bytes, str]:
    """Generate Prometheus metrics response."""
    return generate_latest(), CONTENT_TYPE_LATEST
